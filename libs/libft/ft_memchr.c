#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*array_1;
	size_t				counter;

	counter = 0;
	array_1 = s;
	while (counter < n)
	{
		if (array_1[counter] == (unsigned char)c)
			return ((void *)&array_1[counter]);
		counter++;
	}
	return (NULL);
}
