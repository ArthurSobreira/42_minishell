#include "libft.h"

int	ft_tolower(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
	{
		if (c >= 'A' && c <= 'Z')
			return (c + 32);
		else
			return (c);
	}
	else
		return (c);
}
