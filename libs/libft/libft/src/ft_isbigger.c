#include "libft.h"

t_bool	is_bigger(char *s1, char *s2)
{
	size_t	i;

	i = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i])
		i++;
	if (s1[i] > s2[i])
		return (TRUE);
	return (FALSE);
}
