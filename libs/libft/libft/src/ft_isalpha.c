#include "libft.h"

t_bool	ft_isalpha(int c)
{
	if ((c >= 'A') && (c <= 'Z'))
	{
		return(TRUE);
	}
	else if ((c >= 'a') && (c <= 'z'))
	{
		return(TRUE);
	}
	else
	{
		return (FALSE);
	}
}
