#include "libft.h"

t_bool	ft_isspace(int c)
{
	if ((c == '\t') || (c == '\n') || (c == '\v') || (c == '\f') || (c == '\r')
		|| (c == ' '))
	{
		return(TRUE);
	}
	else
	{
		return (FALSE);
	}
}
