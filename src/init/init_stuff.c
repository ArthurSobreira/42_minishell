#include "minishell.h"

void	clear_prompt(void)
{
	pid_t	pid;
	char	*argv[2];
	char	**envp;

	pid = fork();
	argv[0] = "/usr/bin/clear";
	argv[1] = NULL;
	envp = get_core()->envp;
	if (pid == 0)
	{
		execve("/usr/bin/clear", argv, envp);
		exit(EXIT_SUCCESS);
	}
	else
		waitpid(pid, NULL, 0);
}

