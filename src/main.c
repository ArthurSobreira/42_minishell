int	main(int argc, char **argv, char **envp)
{
	t_minishell	core;

	(void)argv;
	(void)envp;
	if (argc != 1)
		return (0);
	ft_bzero(&core, sizeof(t_minishell));
	built_in_array(&core);
	readlines(&core);
	return (0);
}
